package Lecture1;

public class Elseifstatement {
    public static void main(String[] args) {
        int a = 30;
        if (a == 20) {
            System.out.println(" in side if ");
        } else if (a == 30) {
            System.out.println(" in side else if ");
        } else if (a == 40) {
            System.out.println(" in side else if ");
        } else {
            System.out.println(" in side else ");
        }
    }
}
